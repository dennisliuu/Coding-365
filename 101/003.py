import math
num1 = float(input("num1: "))
num2 = float(input("num2: "))
def Sum(num1, num2):
    return num1 + num2
def Difference(num1, num2):
    return num1 - num2
def Product(num1, num2):
    return num1 * num2
def Quotient(num1, num2):
    return num1 / num2
print(Sum(num1, num2))
print(Difference(num1, num2))
print(Product(num1, num2))
print(Quotient(num1, num2))
